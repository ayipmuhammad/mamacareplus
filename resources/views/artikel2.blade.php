                        Mengatasi Kontraksi dan Ketidaknyamanan di Akhir Kehamilan : Tips untuk Ibu Hamil
                    </h1>
                </div>
            </div>

            <div>
                <p style="text-indent: 30px;" id="p-atas">
                    Pada trimester akhir kehamilan, tubuh ibu mulai mempersiapkan diri untuk proses persalinan. Salah satu tanda bahwa tubuh sedang mempersiapkan kelahiran adalah munculnya kontraksi. Namun, selain kontraksi, ibu juga bisa mengalami berbagai ketidaknyamanan seperti nyeri perut, punggung, dan kelelahan. Artikel ini akan membahas beberapa cara mengatasi ketidaknyamanan yang sering muncul menjelang persalinan.
                </p>
                
                <!-- LIST -->
                <div class="list">
                    <ol>
                        <li>Mengenali Kontraksi Braxton Hicks dan Kontraksi Asli</li>
                        <p>
                            Kontraksi Braxton Hicks sering disebut sebagai “kontraksi palsu” dan umumnya tidak terlalu menyakitkan. Kontraksi ini muncul secara tidak teratur dan hilang dengan sendirinya setelah ibu beristirahat. Berbeda dengan kontraksi asli yang terasa lebih kuat, terjadi secara teratur, dan semakin intens mendekati waktu persalinan.
                        </p>
                        <p>Tips:</p>
                        <ul>
                            <li>
                                Cobalah mengubah posisi atau berjalan-jalan untuk meredakan Braxton Hicks.
                            </li>
                            <li>
                                Jika kontraksi semakin kuat dan tidak hilang dengan istirahat, segera konsultasikan dengan dokter atau bidan.
                            </li>
                        </ul>
                        <li>Mengurangi Nyeri Punggung dan Perut</li>
                        <p>
                            Nyeri di bagian punggung dan perut sering dialami oleh ibu hamil, terutama saat janin tumbuh semakin besar. Hal ini disebabkan oleh peregangan ligamen dan tekanan tambahan pada tulang belakang.
                        </p>
                        <p>Tips:</p>
                        <ul>
                            <li>
                                Gunakan bantal khusus kehamilan saat tidur untuk menopang tubuh.
                            </li>
                            <li>
                                Lakukan peregangan ringan atau yoga prenatal untuk meredakan ketegangan otot.
                            </li>
                            <li>
                                Kompres hangat dapat membantu meredakan nyeri pada punggung bagian bawah.
                            </li>
                        </ul>
                        <li>Menghadapi Kelelahan</li>
                        <p>
                            Seiring dengan bertambahnya berat janin dan perubahan hormon, ibu hamil mungkin merasa cepat lelah. Ini normal, terutama pada trimester akhir.
                        </p>
                        <p>Tips:</p>
                        <ul>
                            <li>
                                Jangan ragu untuk beristirahat kapan pun merasa lelah.
                            </li>
                            <li>
                                Minum banyak air dan makan camilan sehat untuk menjaga energi.
                            </li>
                            <li>
                                Hindari aktivitas berat dan delegasikan tugas rumah tangga kepada pasangan atau keluarga.
                            </li>
                        </ul>
                        <li>Memperhatikan Tanda Persalinan Dini</li>
                        <p>
                            Ketika kontraksi menjadi semakin sering dan kuat, bisa jadi itu tanda bahwa persalinan sudah dekat. Tanda-tanda lain seperti pecahnya air ketuban atau keluarnya lendir bercampur darah juga merupakan indikasi bahwa ibu harus segera pergi ke rumah sakit atau klinik bersalin.
                        </p>
                        <p>Tips:</p>
                        <ul>
                            <li>
                                Persiapkan tas bersalin dan dokumen penting agar siap kapan pun dibutuhkan.
                            </li>
                            <li>
                                Diskusikan dengan dokter atau bidan tentang rencana persalinan yang diinginkan.
                            </li>
                        </ul>
                        <li>Mendapatkan Dukungan dari Keluarga dan Tenaga Medis</li>
                        <p>
                            Menghadapi ketidaknyamanan di akhir kehamilan bisa menjadi tantangan emosional dan fisik. Dukungan dari pasangan, keluarga, dan tenaga medis sangat penting untuk membantu ibu merasa lebih tenang dan siap menghadapi persalinan.
                        </p>
                    </ol>
                </div>

                <p style="text-indent: 30px;" id="p-bawah">
